package br.com.combinado.atividade.calculadora;

public class Calc {

	public int dividir(int primeiroNumero, int segundoNumero) {
		int divisao = primeiroNumero / segundoNumero;
		return divisao;
	}

	public int multiplicar(int primeiroNumero, int segundoNumero) {
		int multiplicacao = primeiroNumero * segundoNumero;
		return multiplicacao;
	}

	public int subtrair(int primeiroNumero, int segundoNumero) {
		int subtrair = primeiroNumero - segundoNumero;
		return subtrair;
	}

	public int somar(int primeiroNumero, int segundoNumero) {
		int adicao = primeiroNumero + segundoNumero;
		return adicao;
	}

}
